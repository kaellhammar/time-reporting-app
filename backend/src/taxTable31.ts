function lookupTable31(income: number): number {
  if (income <= 0) return 0;
  // Above 80,000: percentage-based (state income tax fully applies)
  if (income > 80000) {
    if (income <= 83600) return Math.round(income * 0.32);
    if (income <= 88600) return Math.round(income * 0.33);
    if (income <= 93800) return Math.round(income * 0.34);
    return Math.round(income * 0.35);
  }
  // Binary search for the right bracket
  let lo = 0, hi = BRACKETS.length - 1, tax = 0;
  while (lo <= hi) {
    const mid = (lo + hi) >> 1;
    if (BRACKETS[mid][0] <= income) {
      tax = BRACKETS[mid][1];
      lo = mid + 1;
    } else {
      hi = mid - 1;
    }
  }
  return tax;
}

/**
 * Look up monthly tax from the Swedish tax table.
 * Currently only table 31 is implemented with actual Skatteverket data.
 * Other tables return null (caller should fall back to flat rate).
 */
export function lookupTaxTable(tableNumber: number, monthlyIncome: number): number | null {
  if (tableNumber === 31) return lookupTable31(monthlyIncome);
  return null;
}
